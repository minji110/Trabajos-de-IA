package ArbolBinarioJava;
class Nodo {
    public int dato;
    public Nodo izq, der;

    public Nodo(int dato) {
        this.dato = dato;
        izq = der = null;
    }

    public int getDato() {
        return dato;
    }

    public void setDato(int dato) {
        this.dato = dato;
    }

    public Nodo getIzq() {
        return izq;
    }

    public void setIzq(Nodo izq) {
        this.izq = izq;
    }

    public Nodo getDer() {
        return der;
    }

    public void setDer(Nodo der) {
        this.der = der;
    }
}

